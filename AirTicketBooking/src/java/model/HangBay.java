package model;

/**
 *
 * @author admin
 */
public class HangBay {
    String name;
    String logo;

    public HangBay() {
    }

    public HangBay(String name, String logo) {
        this.name = name;
        this.logo = logo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    @Override
    public String toString() {
        return "HangBay{" + "name=" + name + ", logo=" + logo + '}';
    }
    
}
